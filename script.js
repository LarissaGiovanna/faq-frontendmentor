function desc1() {
    let resposta1 = document.getElementById('resp1');
    let imgDesc1 = document.getElementById('imgDesc1');

    if (resposta1.style.display == 'none') {
        resposta1.style.display = 'block';
        

    } else {
        resposta1.style.display = "none";
    }
}
